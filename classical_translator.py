def simple_translate(text):
    """白话文 → 古文翻译（使用正则单次替换，避免级联错误）"""
    phrases = sorted(TRANSLATION_DICT.keys(), key=len, reverse=True)
    pattern = re.compile('|'.join(map(re.escape, phrases)))
    return pattern.sub(lambda m: TRANSLATION_DICT[m.group()], text)

